	<h1>The Wealthy Gardener</h1>
	<p>A smooth sea never made a skillful mariner.</p>
	<p>
		Be wary of those who tell you to be content with your conditions in life. Only you can know the
		conditions that will satisfy your soul. And only you can feel the pull of your ambition.
	</p>
	<p>Don’t judge each day by the harvest you reap, but by the seeds that you plant.</p>
	<p>
		If you don’t want to be thought of as a cow, engage your time and don’t be lazy. If you don’t
		want to work like an ox for eternity, use your gifts, work intelligently, and control your
		spending. At the very least, live with dignity and choose to contribute to a worthy cause,
		whatever you decide it to be, by listening to your inner voice.
	</p>
	<p>When the roots are deep, there is no reason to fear the wind.</p>
	<p>
		If you cannot give your free time to the pursuit of your dreams, then the seeds of achievement
		are not in you.
	</p>
	<p>
		It’s not the dirty gardeners who deserve out pity, but rather the clean ones, sitting on their
		porches, waiting for the perfect job while their gardens grow into disarray.
	</p>
</div>
